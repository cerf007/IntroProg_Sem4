#Una persona recibe un préstamo de 10,000.00 de un banco 
#se desea saber cuanto pagará de interés, si el banco le cobra una tasa del 27% anual.

prestamo = 10000
tasa_anual = 0.24
interes = prestamo * tasa_anual

print(f"El interés a pagar del prestamo de {prestamo} con un tasa anual del 24% es de {interes:.2f}")
